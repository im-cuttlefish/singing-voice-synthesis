type SpeechElementName = keyof typeof mapping;
type SpeechElementsMap = Map<SpeechElementName, Blob>;

const names = Object.keys(mapping) as SpeechElementName[];

export const getSpeechElements = async () => {
  const responses = await Promise.all(names.map((x) => fetch(mapping[x])));
  const blobs = await Promise.all(responses.map((x) => x.blob()));

  return new Map(blobs.map((x, i) => [names[i], x])) as SpeechElementsMap;
};
